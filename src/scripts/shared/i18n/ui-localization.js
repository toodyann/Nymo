function preserveWordCase(sourceWord, translatedWord) {
  if (!sourceWord) return translatedWord;
  if (sourceWord.toUpperCase() === sourceWord) {
    return translatedWord.toUpperCase();
  }
  const firstSourceChar = sourceWord.charAt(0);
  if (firstSourceChar && firstSourceChar === firstSourceChar.toUpperCase()) {
    return translatedWord.charAt(0).toUpperCase() + translatedWord.slice(1);
  }
  return translatedWord;
}

